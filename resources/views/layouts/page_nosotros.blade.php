		<div class='texto_uno'>
			<p>
				<strong>El servicio nacional de aprendizaje (SENA)</strong> establece que los aprendices elaboren un proyecto, teniendo en cuenta que al final de su formación entreguen un proyecto funcional, de modo que lo aprendido en la formación lo apliquen en el proyecto.
				
			</p>
		</div>
		<hr>
		<div class='texto_dos'>
			<p>	<h5> ¿Qué problemas trae esto? </h5>
				Los aprendices al momento de formular una idea de proyecto, deben partir desde un problema sea social, cultural, empresarial, etc. Es necesario recalcar que esto trae dificultades a los aprendices, como por ejemplo: la indecisión de encontrar un problema, la inexperiencia empresarial, la viabilidad, etc. A su vez se le agrega los deberes personales y de formación. A causa de todo esto los aprendices experimentan estrés, irritabilidad, dolor de cabeza, malas decisiones, indecision, etc.  
			</p>
		</div>
		<hr>
		<div class='texto_tres'>
			<p>
				<h5>¿Qué solución hay para esto?</h5>
				<p>
					Partiendo de esta problematica, se ha desarrollado la plataforma web <strong>SenaProject</strong>. La plataforma está orientada hacia los los aprendices del <strong>Sena</strong> y tanto los aprendices como las <strong>Empresas</strong> y <strong> Pymes</strong> pueden registrarse, tanto las pymes como las empresas pueden postular ideas de proyectos con base a sus necesidades, con el objetivo de que los aprendices elijan sus proyectos formativos con un máximo de 3 integrantes. 
				</p>
			</p>
		</div>
		<div class='texto_cuatro'>
			<p>
				<h5>¿Qué ventajas y oportunidades hay? </h5>
				
				Teniendo en cuenta que los proyectos se van hacer con base a las necesidades de las pymes y empresas, Como resultado aumenta la posibilidad de llegar a una negociación con la pyme o empresa, para una venta, etc. Así mismo acerca a los aprendices al mundo laboral en la manera como se trabaja, como por ejemplo: tomar los requisitos del cliente, exponer avances, etc. En vista de los ya mencionados problemas de elegir un proyecto, por medio de la plataforma web, el aprendiz ya tiene justificado el proyecto de formación.
			</p>
		</div>
		<hr>
		<div class='texto_cinco'>
			<p>
				<h5>En la formación piden un plus para el proyecto, ¿Esto se verá afectado?</h5>
				
				De acuerdo con el propósito del plus y de lo que promueve el <strong>SENA</strong> como <strong>SENNOVA</strong>(Sistema de Investigación, Desarrollo Tecnológico e Innovación), en las ideas y necesidades publicadas por las pymes y empresas, muchos grupos de aprendices en máximo de a 3 por grupo, pueden postular soluciones a estas necesidades, esto quiere decir que un reto o necesidad puede tener muchos grupos, esto conlleva a los aprendices a ser más creativos con las soluciones y a cierta competición entre los grupos, se debe agregar que un proyecto mejor formulado, avanzado y que cumpla con los requisitos, tiene más probabilidad de venderse a una empresa o pyme. 
			</p>
